package org.section3.operators;

public class Ternary_Operator {
    public static void main(String[] args) {
        System.out.println("The ternary operator provides a shorthand way to write if-else statements.\n" +
                "\n" +
                "java\n" +
                "Copy code\n");
        int a = 10, b = 5;
        String result = (a > b) ? "a is greater" : "b is greater";
        System.out.println(result); // Output: "a is greater"
    }
}
